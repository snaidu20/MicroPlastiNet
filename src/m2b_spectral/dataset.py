"""
dataset.py — PyTorch Dataset for spectral polymer classification.

Supports two data sources:
1. Synthetic spectra generated by synthetic_spectra.py (default, sandbox-safe)
2. Real Rochman Lab CSV files from data/raw/rochman_slopp/ (when available)

Real data format expected (Rochman SLoPP/FLOPP CSV):
    - Rows: individual spectra
    - Column 0: label (polymer name string)
    - Columns 1..N: absorbance/intensity values at wavenumber grid points
    - Or separate wavenumber header row (auto-detected)

Usage:
    from dataset import SpectraDataset, get_dataloaders
    train_loader, val_loader, test_loader = get_dataloaders(split_seed=42)
"""

import os
import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader, WeightedRandomSampler
from sklearn.model_selection import StratifiedShuffleSplit
from typing import Optional, Tuple, List, Dict
import glob

from synthetic_spectra import (
    generate_dataset,
    POLYMER_CLASSES,
    CLASS_TO_IDX,
    IDX_TO_CLASS,
    N_POINTS,
    WAVENUMBERS,
)

# ── Default paths ─────────────────────────────────────────────────────────────
_BASE = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
SYNTHETIC_CACHE = os.path.join(_BASE, "data", "processed", "m2b", "synthetic_spectra.npz")
ROCHMAN_DIR     = os.path.join(_BASE, "data", "raw", "rochman_slopp")


class SpectraDataset(Dataset):
    """
    PyTorch Dataset wrapping spectral data (synthetic or real).

    Parameters
    ----------
    X : np.ndarray, shape (N, 901)  — float32 spectra
    y : np.ndarray, shape (N,)      — int64 class labels
    augment : bool
        If True, apply on-the-fly spectral augmentation (noise, shift).
    aug_seed : int
        Seed for reproducible augmentation.
    """

    def __init__(self, X: np.ndarray, y: np.ndarray,
                 augment: bool = False, aug_seed: int = 0):
        assert len(X) == len(y), "X and y must have same length"
        self.X       = torch.tensor(X, dtype=torch.float32)   # (N, 901)
        self.y       = torch.tensor(y, dtype=torch.long)       # (N,)
        self.augment = augment
        self.rng     = np.random.default_rng(aug_seed)

    def __len__(self) -> int:
        return len(self.y)

    def __getitem__(self, idx: int):
        x = self.X[idx].clone()  # (901,)
        if self.augment:
            x = self._augment(x)
        # Add channel dim for Conv1d: (1, 901)
        return x.unsqueeze(0), self.y[idx]

    def _augment(self, x: torch.Tensor) -> torch.Tensor:
        """
        Lightweight spectral augmentations:
        - Gaussian noise injection (SNR 20–50)
        - Random intensity scaling (±15%)
        - Random wavenumber shift (circular roll, ≤±3 pts = ±12 cm⁻¹)
        """
        # Noise
        snr    = self.rng.uniform(20, 50)
        sigma  = float(x.max()) / snr
        noise  = torch.tensor(self.rng.normal(0, sigma, size=x.shape),
                               dtype=torch.float32)
        x = x + noise

        # Intensity scale
        scale = self.rng.uniform(0.85, 1.15)
        x = x * scale

        # Spectral shift (circular roll in wavenumber space)
        shift = int(self.rng.integers(-3, 4))
        if shift != 0:
            x = torch.roll(x, shift)

        # Clip and renormalize
        x = torch.clamp(x, 0, None)
        mx = x.max()
        if mx > 1e-8:
            x = x / mx
        return x

    @property
    def class_weights(self) -> torch.Tensor:
        """Inverse-frequency class weights for weighted cross-entropy."""
        counts = torch.bincount(self.y, minlength=len(POLYMER_CLASSES)).float()
        weights = 1.0 / (counts + 1e-6)
        return weights / weights.sum() * len(POLYMER_CLASSES)

    @property
    def sample_weights(self) -> np.ndarray:
        """Per-sample weights for WeightedRandomSampler."""
        cw = self.class_weights.numpy()
        return cw[self.y.numpy()]


# ── Real data loader ──────────────────────────────────────────────────────────

def load_rochman_csv(directory: str) -> Tuple[Optional[np.ndarray], Optional[np.ndarray]]:
    """
    Load real Rochman Lab SLoPP / FLOPP CSV files if present.

    Expected file naming: <polymer>_*.csv or a single merged CSV with
    a 'label' column.

    Returns (X, y) arrays or (None, None) if no files found.
    """
    csv_files = glob.glob(os.path.join(directory, "*.csv"))
    if not csv_files:
        return None, None

    X_list, y_list = [], []

    for fp in sorted(csv_files):
        try:
            data = np.genfromtxt(fp, delimiter=",", dtype=str)
        except Exception as e:
            print(f"  [WARN] Could not read {fp}: {e}")
            continue

        # Auto-detect if first column is label
        if data.ndim < 2 or data.shape[1] < 2:
            continue

        # Check if first row is a header
        header_row = 0
        try:
            float(data[0, 1])  # if this fails → row 0 is header
        except ValueError:
            header_row = 1

        rows = data[header_row:]
        for row in rows:
            label_str = row[0].strip().upper()
            # Map to canonical class
            matched = None
            for cls in POLYMER_CLASSES[:-1]:  # exclude "Other"
                if cls in label_str:
                    matched = cls
                    break
            if matched is None:
                matched = "Other"

            try:
                spectrum = row[1:].astype(float).astype(np.float32)
            except Exception:
                continue

            # Interpolate to standard 901-point grid if needed
            if len(spectrum) != N_POINTS:
                src_wn = np.linspace(WAVENUMBERS[0], WAVENUMBERS[-1], len(spectrum))
                spectrum = np.interp(WAVENUMBERS, src_wn, spectrum).astype(np.float32)

            # Normalize
            mx = spectrum.max()
            if mx > 1e-8:
                spectrum /= mx

            X_list.append(spectrum)
            y_list.append(CLASS_TO_IDX[matched])

    if not X_list:
        return None, None

    X = np.stack(X_list).astype(np.float32)
    y = np.array(y_list, dtype=np.int64)
    print(f"[INFO] Loaded {len(X)} real spectra from {directory}")
    print(f"       Class distribution: { {IDX_TO_CLASS[i]: int((y==i).sum()) for i in range(len(POLYMER_CLASSES))} }")
    return X, y


# ── Dataset factory ───────────────────────────────────────────────────────────

def load_or_generate(n_per_class: int = 500,
                     seed: int = 42,
                     use_real_if_available: bool = True
                     ) -> Tuple[np.ndarray, np.ndarray, str]:
    """
    Load cached synthetic data, or generate + cache it.
    If real Rochman data is present, merge it in.

    Returns (X, y, source_description)
    """
    source = "synthetic"

    # Try to load cached synthetic data
    if os.path.exists(SYNTHETIC_CACHE):
        data = np.load(SYNTHETIC_CACHE)
        X, y = data["X"].astype(np.float32), data["y"].astype(np.int64)
        print(f"[INFO] Loaded cached synthetic spectra: {X.shape} from {SYNTHETIC_CACHE}")
    else:
        print("[INFO] Generating synthetic spectra (first run)...")
        X, y = generate_dataset(n_per_class=n_per_class, seed=seed,
                                 save_path=SYNTHETIC_CACHE)

    # Optionally merge real data
    if use_real_if_available and os.path.isdir(ROCHMAN_DIR):
        X_real, y_real = load_rochman_csv(ROCHMAN_DIR)
        if X_real is not None:
            X = np.concatenate([X, X_real], axis=0)
            y = np.concatenate([y, y_real], axis=0)
            source = "synthetic+real"
            print(f"[INFO] Merged real data → total {len(X)} spectra")

    return X, y, source


def get_dataloaders(n_per_class: int = 500,
                    seed: int = 42,
                    val_frac: float = 0.15,
                    test_frac: float = 0.15,
                    batch_size: int = 64,
                    num_workers: int = 0,
                    augment_train: bool = True,
                    use_real_if_available: bool = True
                    ) -> Tuple[DataLoader, DataLoader, DataLoader, Dict]:
    """
    Main entry point: returns (train_loader, val_loader, test_loader, meta).

    meta dict includes class_weights, class_names, n_train/val/test, source.

    Stratified split ensures class balance across all folds.
    """
    X, y, source = load_or_generate(
        n_per_class=n_per_class, seed=seed,
        use_real_if_available=use_real_if_available
    )

    # Stratified train/temp split
    sss = StratifiedShuffleSplit(n_splits=1,
                                  test_size=val_frac + test_frac,
                                  random_state=seed)
    train_idx, temp_idx = next(sss.split(X, y))

    # Split temp into val + test
    val_ratio_of_temp = val_frac / (val_frac + test_frac)
    sss2 = StratifiedShuffleSplit(n_splits=1,
                                   test_size=1 - val_ratio_of_temp,
                                   random_state=seed)
    val_idx_rel, test_idx_rel = next(sss2.split(X[temp_idx], y[temp_idx]))
    val_idx  = temp_idx[val_idx_rel]
    test_idx = temp_idx[test_idx_rel]

    X_train, y_train = X[train_idx], y[train_idx]
    X_val,   y_val   = X[val_idx],   y[val_idx]
    X_test,  y_test  = X[test_idx],  y[test_idx]

    train_ds = SpectraDataset(X_train, y_train, augment=augment_train, aug_seed=seed)
    val_ds   = SpectraDataset(X_val,   y_val,   augment=False)
    test_ds  = SpectraDataset(X_test,  y_test,  augment=False)

    # Weighted sampler for training (handles class imbalance)
    sample_wts   = train_ds.sample_weights
    sampler      = WeightedRandomSampler(
        weights=torch.tensor(sample_wts, dtype=torch.float32),
        num_samples=len(train_ds),
        replacement=True
    )

    train_loader = DataLoader(train_ds, batch_size=batch_size,
                              sampler=sampler, num_workers=num_workers,
                              pin_memory=False)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size,
                              shuffle=False, num_workers=num_workers)
    test_loader  = DataLoader(test_ds,  batch_size=batch_size,
                              shuffle=False, num_workers=num_workers)

    meta = {
        "source":        source,
        "n_train":       len(train_ds),
        "n_val":         len(val_ds),
        "n_test":        len(test_ds),
        "n_classes":     len(POLYMER_CLASSES),
        "class_names":   POLYMER_CLASSES,
        "class_weights": train_ds.class_weights,
        "input_dim":     N_POINTS,
        "wavenumbers":   WAVENUMBERS,
    }

    print(f"[INFO] DataLoaders ready | train={meta['n_train']} "
          f"val={meta['n_val']} test={meta['n_test']} | source={source}")
    return train_loader, val_loader, test_loader, meta
